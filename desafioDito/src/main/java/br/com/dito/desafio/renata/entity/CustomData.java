package br.com.dito.desafio.renata.entity;

public class CustomData {
	private String key;
	private String value;
	
	public CustomData(){}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
